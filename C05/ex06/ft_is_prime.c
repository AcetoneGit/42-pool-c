int	ft_is_prime(int nb)
{
	int	i;

	if (nb < 3 || (!nb % 2))
		return (nb == 2);
	i = 2;
	while (i * i <= nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
